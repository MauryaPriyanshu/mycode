// Switch Statement
public class SwitchState{
	public static void main(String[] args){
		int num = 10;
		switch(num)
		{
			case 0:
			System.out.println("Number is 0");
			break;
			case 1:
			System.out.println("Number is 1");
			break;
			case 2:
			System.out.println("Number is 2");
			break;
			default:
			System.out.println("Number is :" +num);
		}
	}
}

